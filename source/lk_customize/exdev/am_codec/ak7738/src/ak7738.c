static unsigned int ak7738_reads(void *handle, unsigned int addr, u8 *reg,
                                 int reglen, u8 *data, int datalen)
{
    int ret;
    ret = hal_i2c_read_reg_data(handle, addr, reg, reglen, data, datalen);
    return ret;
}

static unsigned int ak7738_writes(void *handle, unsigned int addr, u8 *tx,
                                  size_t wlen)
{
    int ret;
    ret = hal_i2c_write_reg_data(handle, addr, tx, 1, &tx[1], wlen - 1);
    return ret;
}

static int ak7738_read_reg(void *handle, unsigned int addr, unsigned int reg,
                           void *data)
{
    unsigned char tx[3] = {0}, rx[3] = {0};
    int wlen, rlen;
    int ret;
    char *p = (char *)data;
    wlen = 3;
    rlen = 1;
    dprintf(CODEC_7738_DBG_PRT,
            "%s: result1: read i2c ext addr=0x%x,reg=0x%x 0x%x\n", __func__,
            addr, reg, rx[0]);
    tx[0] = (unsigned char)(AK7738_COMMAND_READ_REG & 0x7F);
    tx[1] = (unsigned char)(0xFF & (reg >> 8));
    tx[2] = (unsigned char)(0xFF & reg);
    ret = hal_i2c_read_reg_data(handle, addr, tx, wlen, rx, rlen);
    dprintf(CODEC_7738_DBG_PRT,
            "%s: result: read i2c ext addr=0x%x,reg=0x%x 0x%x\n", __func__,
            addr, reg, rx[0]);
    if (ret < 0) {
        dprintf(0, "%s: error: read ak7738 reg=%x,ret=%x\n", __func__, reg,
                ret);
        return ret;
    }
    p[0] = rx[0] & 0xff;
    return ret;
}

static int ak7738_write_reg(void *handle, unsigned int addr, unsigned int reg,
                            unsigned int val)
{
    uint8_t tx[4];
    int wlen;
    int ret;
    dprintf(CODEC_7738_DBG_PRT, "[AK7738] %s (%XH, %XH)\n",
       __FUNCTION__, reg, val); 
    wlen = 4;
    tx[0] = (unsigned char)AK7738_COMMAND_WRITE_REG;
    tx[1] = (unsigned char)(0xFF & (reg >> 8));
    tx[2] = (unsigned char)(0xFF & reg);
    tx[3] = (unsigned char)(0xFF & val);
    ret = hal_i2c_write_reg_data(handle, addr, tx, 1, &tx[1], wlen - 1);
    if (ret < 0) {
        dprintf(0, "%s: error: %p reg=0x%x, val=0x%x\n", __func__, handle, reg,
                val);
    }
    return ret;
}

static int crc_read(void *handle, unsigned int addr)
{
    int rc;
    u8 tx[3], rx[2];
    tx[0] = COMMAND_CRC_READ;
    tx[1] = 0;
    tx[2] = 0;
    rc = ak7738_reads(handle, addr, tx, 3, rx, 2);
    return (rc < 0) ? rc : ((rx[0] << 8) + rx[1]);
}

int ak7738_status = POWERDOWN;
static int ak7738_set_status(void *handle, unsigned int addr,
                             enum ak7738_status status)
{
    u8 data = 0;
    int ret;
    switch (status) {
    case RUN1:
        ret = ak7738_read_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                              &data);
        if (ret < 0) {
            return -1;
        }
        ret = ak7738_write_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                               (0x80 | data));
        thread_sleep(10);
        ret = ak7738_read_reg(handle, addr, AK7738_85_RESET_CONTROL, &data);
        ret = ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL,
                               updatebit(data, 0x99, 0x19));
        break;
    case RUN2:
        ret = ak7738_read_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                              &data);
        ret = ak7738_write_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                               (0x80 | data));
        thread_sleep(10);
        ret = ak7738_read_reg(handle, addr, AK7738_85_RESET_CONTROL, &data);
        ret = ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL,
                               updatebit(data, 0x95, 0x15));
        break;
    case RUN3:
        ret = ak7738_read_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                              &data);
        ret = ak7738_write_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                               (0x80 | data));
        thread_sleep(10);
        ret = ak7738_read_reg(handle, addr, AK7738_85_RESET_CONTROL, &data);
        ret = ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL,
                               updatebit(data, 0x93, 0x13));
        break;
    case DOWNLOAD:
        ret = ak7738_read_reg(handle, addr, AK7738_85_RESET_CONTROL, &data);
        ret = ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL,
                               (0x80 | data));
        thread_sleep(1);
        break;
    case DOWNLOAD_FINISH:
        ret = ak7738_read_reg(handle, addr, AK7738_85_RESET_CONTROL, &data);
        ret = ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL,
                               updatebit(data, 0x80, 0x0));
        thread_sleep(1);
        break;
    case STANDBY:
        ret = ak7738_read_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                              &data);
        ret = ak7738_write_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                               (0x80 | data));
        thread_sleep(10);
        ret = ak7738_read_reg(handle, addr, AK7738_85_RESET_CONTROL, &data);
        ret = ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL,
                               updatebit(data, 0x9F, 0x0));
        break;
    case SUSPEND:
    case POWERDOWN:
        ret = ak7738_read_reg(handle, addr, AK7738_85_RESET_CONTROL, &data);
        ret = ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL,
                               updatebit(data, 0x9F, 0x0));
        ret = ak7738_read_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                              &data);
        ret = ak7738_write_reg(handle, addr, AK7738_01_SYSTEM_CLOCK_SETTING_2,
                               updatebit(data, 0x80, 0x0));
        break;
    default:
        return -1;
    }
    ak7738_status = status;
    return 0;
}

static int ak7738_ram_download(void *handle, unsigned int addr, u8 *tx_ram,
                               u64 num, u16 crc)
{
    int rc;
    u8 nDSPRun;
    u16 read_crc;
    dprintf(CODEC_7738_DBG_PRT,"\t[AK7738] %s num=%ld addr=0x%x handle=%p\n", __FUNCTION__,
           (long int)num, addr, handle);
    rc = ak7738_read_reg(handle, addr, AK7738_85_RESET_CONTROL, &nDSPRun);
    if (rc < 0) {
        printf("\tErr [AK7738] %s num=%ld\n", __FUNCTION__, (long int)num);
        return rc;
    }
    ak7738_set_status(handle, addr, DOWNLOAD);
    rc = ak7738_writes(handle, addr, tx_ram, num);
    if (rc < 0) {
        ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL, nDSPRun);
        return rc;
    }
    if ((crc != 0) && (rc >= 0)) {
        read_crc = crc_read(handle, addr);
        printf("\t[AK7738] %s CRC Cal=%x Read=%x\n", __FUNCTION__, (int)crc,
               (int)read_crc);
        if (read_crc == crc)
            rc = 0;
        else
            rc = -1;
    }
    ak7738_write_reg(handle, addr, AK7738_85_RESET_CONTROL, nDSPRun);
    return rc;
}

static int calc_CRC(int length, u8 *data)
{
#define CRC16_CCITT (0x1021)
    unsigned short crc = 0x0000;
    int i, j;
    for (i = 0; i < length; i++) {
        crc ^= *data++ << 8;
        for (j = 0; j < 8; j++) {
            if (crc & 0x8000) {
                crc <<= 1;
                crc ^= CRC16_CCITT;
            } else {
                crc <<= 1;
            }
        }
    }
    return crc;
}

static int ak7738_write_ram(void *handle, unsigned int addr,
                     int nPCRam, // 0 : PRAM, 1 : CRAM, 2:  OFREG
                     u8 *upRam, int nWSize)
{
    int n, ret;
    int wCRC;
    int nMaxSize;
    dprintf(CODEC_7738_DBG_PRT, "[EXT CMD for AK7738] %s RamNo=%d, len=%d\n",
            __FUNCTION__, nPCRam, nWSize);
    switch (nPCRam) {
    case RAMTYPE_PRAM:
        if (upRam[0] == COMMAND_WRITE_PRAM_SUB) {
            nMaxSize = TOTAL_NUM_OF_SUB_PRAM_MAX;
        } else {
            nMaxSize = TOTAL_NUM_OF_PRAM_MAX;
        }
        if (nWSize > nMaxSize) {
            printf("%s: PRAM Write size is over! \n", __func__);
            return (-1);
        }
        break;
    case RAMTYPE_CRAM:
        if (upRam[0] == COMMAND_WRITE_CRAM_SUB) {
            nMaxSize = TOTAL_NUM_OF_SUB_CRAM_MAX;
        } else {
            nMaxSize = TOTAL_NUM_OF_CRAM_MAX;
        }
        if (nWSize > nMaxSize) {
            printf("%s: CRAM Write size is over! \n", __func__);
            return (-1);
        }
        break;
    case RAMTYPE_OFREG:
        if (nWSize > TOTAL_NUM_OF_OFREG_MAX) {
            printf("%s: OFREG Write size is over! \n", __func__);
            return (-1);
        }
        break;
    default:
        break;
    }
    wCRC = calc_CRC(nWSize, upRam);
    n = MAX_LOOP_TIMES;
    do {
        ret = ak7738_ram_download(handle, addr, upRam, nWSize, wCRC);
        if (ret >= 0)
            break;
        n--;
    } while (n > 0);
    if (ret < 0) {
        printf("%s: RAM Write Error! RAM No = %d \n", __func__, nPCRam);
        return (-1);
    }
    return (0);
}

static int ak7738_firmware_write_ram(void *handle, unsigned int addr, u16 mode,
                              u16 cmd)
{
    int ret = 0;
    int nNumMode, nMaxLen;
    int nRamSize;
    // int data_size;
    u8 *ram_basic;
    // const struct firmware *fw;
    // u8 *fwdn;
    char szFileName[128];
    dprintf(CODEC_7738_DBG_PRT, "[AK7738] %s mode=%d, cmd=%d\n", __FUNCTION__,
            mode, cmd);
    switch (mode) {
    case RAMTYPE_PRAM:
        nNumMode =
            sizeof(ak7738_firmware_pram) / sizeof(ak7738_firmware_pram[0]);
        break;
    case RAMTYPE_CRAM:
        nNumMode =
            sizeof(ak7738_firmware_cram) / sizeof(ak7738_firmware_cram[0]);
        break;
    case RAMTYPE_OFREG:
        nNumMode =
            sizeof(ak7738_firmware_ofreg) / sizeof(ak7738_firmware_ofreg[0]);
        break;
    default:
        printf("[AK7738] %s mode Error=%d\n", __FUNCTION__, mode);
        return (-1);
    }
    if (cmd == 0)
        return (0);
    if (cmd >= nNumMode) {
        printf("%s: invalid command %d\n", __func__, cmd);
        return (-1);
    }
    switch (mode) {
    case RAMTYPE_PRAM:
        sprintf(szFileName, "early_app/audio/ak7738_pram_%s.bin",
                ak7738_firmware_pram[cmd]);
        nMaxLen = TOTAL_NUM_OF_PRAM_MAX;
        break;
    case RAMTYPE_CRAM:
        sprintf(szFileName, "early_app/audio/ak7738_cram_%s.bin",
                ak7738_firmware_cram[cmd]);
        nMaxLen = TOTAL_NUM_OF_CRAM_MAX;
        break;
    case RAMTYPE_OFREG:
        sprintf(szFileName, "early_app/audio/ak7738_ofreg_%s.bin",
                ak7738_firmware_ofreg[cmd]);
        nMaxLen = TOTAL_NUM_OF_OFREG_MAX;
        break;
    default:
        return (-1);
    }
    nRamSize = res_size(szFileName);
    ram_basic = (uint8_t *)memalign(32, nRamSize);
    ret = res_load(szFileName, ram_basic, ROUNDUP(nRamSize, 32), 0);
    if (ret < 0) {
        printf("[AK7738] %s could not load firmware=%d\n", szFileName, ret);
        return -1;
    }
    dprintf(CODEC_7738_DBG_PRT, "[AK7738] %s name=%s size=%d\n", __FUNCTION__,
            szFileName, nRamSize);
    if (nRamSize > nMaxLen) {
      printf("[AK7738] %s RAM Size Error : %d\n", __FUNCTION__, (int)nRamSize);
      free(ram_basic);
      return -1;
    }
    ret = ak7738_write_ram(handle, addr, (int)mode, ram_basic, nRamSize);
    free(ram_basic);
    return ret;
}
