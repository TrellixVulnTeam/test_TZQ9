const edc_curve_t *crypto_get_edc_curve(void)
{
    const edc_curve_t *curve = NULL;

    curve = &ed25519;

    return curve;
}

const ecc_curve_t *crypto_get_ecc_curve(U32 prime_sz)
{
    const ecc_curve_t *curve = NULL;

    if (24 == prime_sz) {
        curve = &ec_p192r1;
    } else if (32 == prime_sz) {
        curve = &ec_p256r1;
    } else if (48 == prime_sz) {
        curve = &ec_p384r1;
    } else if (65 == prime_sz || 66 == prime_sz) {
        curve = &ec_p521r1;
    }

    return curve;
}

const ecc_curve_t *crypto_get_sm2_curve(uint32_t prime_sz)
{
    const ecc_curve_t *curve = NULL;

    if (32u == prime_sz) {
        curve = &sm2_p256v1;
    }

    return curve;
}

bool is_sm2_curve(const ecc_curve_t *curve)
{
    return (NULL != curve) && (curve->curve_id == EC_sm2p256v1);
}
